package com.hot6.admin.vo;

public class NoticeVO {
	
	private int noId;
	private String noTextName;
	private String noMainText;
	private String noTextTime;
	private String noWriter;
	
	
	public String getNoWriter() {
		return noWriter;
	}
	public void setNoWriter(String noWriter) {
		this.noWriter = noWriter;
	}
	public int getNoId() {
		return noId;
	}
	public void setNoId(int noId) {
		this.noId = noId;
	}
	public String getNoTextName() {
		return noTextName;
	}
	public void setNoTextName(String noTextName) {
		this.noTextName = noTextName;
	}
	public String getNoMainText() {
		return noMainText;
	}
	public void setNoMainText(String noMainText) {
		this.noMainText = noMainText;
	}
	public String getNoTextTime() {
		return noTextTime;
	}
	public void setNoTextTime(String noTextTime) {
		this.noTextTime = noTextTime;
	}
		
}
